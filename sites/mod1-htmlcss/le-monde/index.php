						Le journal revient du retentissement que cet incident de jeu avait eu dans le monde entier grâce
						à la diffusion d'une vidéo sur Internet.
						<q
							>Ce coup de genou a constitué un apport très important de la Bolivie à la campagne mondiale
							visant à favoriser le fair-play. Nous avons montré par l'exemple de ce qu'il ne faut pas
							faire, en football comme en politique</q
						>, écrit le journal.
					</p>
